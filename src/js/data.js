/**
 * Returns the color strip color for a subject name.
 * Checks longer keywords first to avoid 'מבני' matching before 'מבני נתונים'.
 * @param {string} subjectName
 * @returns {string} CSS color
 */
export function getSubjectColor(subjectName) {
  if (!subjectName) return 'transparent';
  // Sort by keyword length descending so longer/more-specific keys match first
  const sorted = Object.entries(SUBJECT_COLORS).sort((a, b) => b[0].length - a[0].length);
  for (const [keyword, color] of sorted) {
    if (subjectName.includes(keyword)) return color;
  }
  return '#9e9e9e';
}

